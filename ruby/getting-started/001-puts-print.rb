# puts(プットエス)はコンピュータに文字列を表示させる記述（きじゅつ）です
# putsは改行（かいぎょう）されて表示されるに対し、printは改行されないで表示されます
# putsは「p」と省略することができる
# 半角スペース、ダブルクオーテーション、グッドモーニング
puts "Good Morning"

puts "Good Afternoon"

puts "Good Evening"

# print
# printは改行されないで表示されます
print "Good Morning"

print "Good Affternoon"

print "Good Evening"