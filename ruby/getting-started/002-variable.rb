# 変数
# 変数にデータを入れることを「代入（だいにゅう）」といい、取り出すことを「参照（さんしょう）」と言います。
# 変数には名前をつけることができ、変数名と言います
# 変数を作ることを「変数を宣言する」と言います

# 変数名には、アルファベット、数字、アンダースコア(_)が使える
# 変数名は、数字から始めることができない

# 変数のnumに1という整数を代入する
num = 1

puts num

